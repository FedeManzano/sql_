USE db_bares;

GO
CREATE PROCEDURE sp_Insertar_Persona
@DNI CHAR(8),
@NOMBRE VARCHAR(30)
AS
BEGIN
	BEGIN TRANSACTION
	BEGIN TRY
			
		
		INSERT INTO Persona(Dni, NombrePersona)
		VALUES
		(@DNI, UPPER(@NOMBRE))

		COMMIT TRANSACTION
	END TRY
	BEGIN CATCH
		IF @@TRANCOUNT > 0
			ROLLBACK
	END CATCH
END
GO

